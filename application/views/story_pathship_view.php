            <p>May, 2015 - I am moving to Hong Kong to live here and work from our office after more than a year of working remotely from Slovakia. Meeting team members in person. Understanding frustrations on Hong Kong education market motivated us to start a company. Unfair commissions, difficult to find right person were only few of them. Despite the fact that education is moving toward online world, we believed in the power of human aspect and personalised experiences with focus on problem solving.</p>

            <p>I was the first employee hired. Later, we grew from team of 3 to the team of 20. We started in customer segment connecting students and tutors. Product on which you work doesn’t always have to be an app. We called our initial product as <span>WhatsApp-based</span> - we were proud of a community behind us and we were helping them using channels they were using - WhatsApp, Facebook, emails. This helped us understand their pains and needs what helped us to build a platform to solve most frustrating parts of learning process - to find a right tutor and to make learning affordable, effective and enjoyable.</p>

            <p>As our team was growing, we were looking for ways to stay effective at work and transparent in communication. From flat structure we’ve started verticalizing and introduced a layer of management to coordinate internal teams. As more developers were contributing to code base, we started taking quality assurance more seriously. Design team also got bigger. As there ware more pairs of eyes looking at the pixels, there was more ideas and we became more precise in communicating them with designers, engineers and other folks in company. We’ve worked with external design and development agencies and studios bringing their point of view into challenges we were solving. </p>
                
            
