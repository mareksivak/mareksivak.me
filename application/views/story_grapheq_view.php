            <p>My first interviews didn’t go well. I was nervous and people didn’t find what we’re working on useful. Some of them found it scary. If people are not excited about your product there might be two reasons - no one needs your product, or keep searching for the right people. I’ve been carefully writing notes from each interview - almost everything what was spoken was captured on my paper - my notes became a collection of information about people, language they use to talk about their problems and our product. I’ve been reviewing them, searching for patterns in answers and looking for new ways to explore. From these data I was building personas which represented people I was talking to.</p>
            
            <p>Later, I found types of people who found our product useful. I focused on them more. Besides learning more about them, iterating our designs it resulted in a community of great folks excited about our product and happy to give their help.</p>
            
            <p>When we felt we’ve collected enough data we made a decision - we’ll use our tech to help sales professionals to better understand their meetings based on emotional data. I was learning how sales professionals work, how they think and intensively interviewing various people from different positions, raising questions and adapting our app to match consumer expectations. We needed to balance what users are expecting and resources of our engineering team. It was very challenging to find the best way to enter the market.</p>
